
public class NestedLoopsAc1 {

	public static void main(String[] args) {
		int counter = 1;
		int col = 1;
		for (counter = 1; counter <=5; counter++)
		{
			for (col =1; col <= 4; col++)
			{
				System.out.print(counter * col + " ");
			
			}
			System.out.println("");
			
				
			
		
		}

	}

}
